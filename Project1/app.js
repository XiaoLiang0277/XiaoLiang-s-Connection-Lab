

function playtext() {
    var text = document.getElementById("textplace");
    text.style.display = "block";
  }



function showcatchy() {
    document.getElementById('catchy')
        .style.display = "block";
    document.getElementById('catchyID')
        .style.display = "none";
}

function showspider() {
  document.getElementById('spiders')
      .style.display = "block";
  document.getElementById('spidersID')
      .style.display = "none";
}